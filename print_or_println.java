public class print_or_println {
    public static void main(String[] args) {
        // Mencetak teks "Disini hujan," diikuti dengan baris baru
        System.out.println("Disini hujan, ");
        // Mencetak teks "Mari pakai jas hujan." diikuti dengan baris baru
        System.out.println("Mari pakai jas hujan.");
        
        System.out.println();
        
        // Mencetak teks "Disini hujan," tanpa baris baru
        System.out.print("Disini hujan, ");
        // Mencetak teks "Mari pakai jas hujan." tanpa baris baru
        System.out.print("Mari pakai jas hujan.");
    }
}
